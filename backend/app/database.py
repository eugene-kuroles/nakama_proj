from sqlalchemy import create_engine
from sqlalchemy.orm import sessionmaker, declarative_base

# ==============================================================================
# –ù–∞—Å—Ç—Ä–æ–π–∫–∏ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è
# ==============================================================================

# –ü—É—Ç—å –∫ —Ñ–∞–π–ª—É –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö (—Å–æ–∑–¥–∞—Å—Ç—Å—è –≤ –ø–∞–ø–∫–µ backend/)
# sqlite:/// ‚Äî –æ–∑–Ω–∞—á–∞–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å SQLite
# ./snake.db ‚Äî —Ñ–∞–π–ª –±—É–¥–µ—Ç —Å–æ–∑–¥–∞–Ω –≤ —Ç–µ–∫—É—â–µ–π –ø–∞–ø–∫–µ (backend/)
SQLALCHEMY_DATABASE_URL = "sqlite:///./snake.db"

